 const jugadores = [];
function agregarDato() {

   

    const inputElement = document.getElementById('nuevoJugador');
    const dato = inputElement.value.trim();
   
    
    if (dato !== '') {
        const $listaElement = document.getElementById('listaJugador');
        const nuevoDato = document.createElement('li');
        
        nuevoDato.textContent = dato;
        nuevoDato.addEventListener('click', function() {
            $listaElement.removeChild(this);
            jugadores.splice(jugadores.indexOf(dato), 1);
        });
        
        $listaElement.appendChild(nuevoDato);
    } 
 
    jugadores.push(dato);
    inputElement.value = "";
};




